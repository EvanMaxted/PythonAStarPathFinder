# import simple graphics and modules
from SimpleGraphics import*
import mazeDrawer
import aStar

'''
function : main
purpose : call all the modules
            - show title and instruction screen
            - draw maze
            - user picks start and end
            - user picks where the barriers are
            - finds shortest path
            - draws shortest path
in : maze
return : none
'''
def main(maze):
    mazeDrawer.titleScreenDraw(loadImage('titlescreen.png')) # draws titlescreen
    mazeDrawer.mazeInit(maze) # draws blank map
    start = mazeDrawer.mouseInput() # gets where user wants start node
    mazeDrawer.startDraw(start[0],start[1]) # draws start node
    sleep(1) # pauses for 1 sec to give user time to 'unclick'
    goal = mazeDrawer.mouseInput() # gets where user wants end node
    mazeDrawer.goalDraw(goal[0],goal[1]) # draws goal node
    sleep(1) # pauses for 1 sec to give user time to 'unclick'
    mazeDrawer.wallDraw(maze) # walls get drawn and put into the maze
    path = aStar.aStarSearch(maze, start, goal) #path is found from start to end
    if path == None: # if there is no path
        return
    mazeDrawer.pathDraw(path[1:-1]) # draw path minus start and end nodes

